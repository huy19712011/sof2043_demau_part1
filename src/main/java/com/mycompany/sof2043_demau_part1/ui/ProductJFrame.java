    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        // click to row of table
        int selectedRow = jTable1.getSelectedRow();
        Object selectedObject = jTable1.getValueAt(selectedRow, 0);
        int id = Integer.parseInt(selectedObject.toString());
        Product selectedProduct = productDao.findById(id);

        jTextField1.setText(String.valueOf(selectedProduct.getId()));
        jTextField2.setText(selectedProduct.getName());
        jTextField3.setText(String.valueOf(selectedProduct.getPrice()));

        // set selected item for combobox
        long categoryId = selectedProduct.getCategoryId();
        jComboBox1.setSelectedIndex((int) categoryId - 1);

    }//GEN-LAST:event_jTable1MouseClicked

