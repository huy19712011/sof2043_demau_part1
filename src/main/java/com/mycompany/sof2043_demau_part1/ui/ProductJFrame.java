    private void fillComboBox() {

        // set up data for combobox
        List<Category> categories = categoryDao.findAll();

//        jComboBox1.removeAllItems();
        for (var category : categories) {
            jComboBox1.addItem(category.getName());
        }
    }

    private void fillTable() {

        defaultTableModel.setRowCount(0);

        // rows
        List<Product> products = productDao.findAll();
        products.forEach(System.out::println);

        for (Product product : products) {

//            long categoryId = product.getCategoryId();
//            Category category = categoryDao.findById(categoryId);
            defaultTableModel.addRow(new Object[]{
                product.getId(),
                product.getName(),
                product.getPrice(),
                //                product.getCategoryId()
                //                category.getName()
                categoryDao.findById(product.getCategoryId()).getName()
            });
        }
    }

    private Product readFromForm() {

        // read data from form
        int id = Integer.parseInt(jTextField1.getText());
        String name = jTextField2.getText();
        float price = Float.parseFloat(jTextField3.getText());

        int selectedIndex = jComboBox1.getSelectedIndex();

        return new Product(id, name, price, selectedIndex + 1);
    }

    private void clearForm() {

        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jComboBox1.setSelectedIndex(0);
    }

